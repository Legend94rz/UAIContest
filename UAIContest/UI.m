function calc()
	global objS1
	global objS2
	global objS3
	global Fe
	res = zeros(size(Fe,1),1);
	s = [ get(objS1,'Value'),get(objS2,'Value'),get(objS3,'Value')]
	ind = Fe.sim>s(1);
	vind =~(ind);
	res(ind) = Fe.meanOfWeek(ind);
	res(vind) = Fe.meanOfHis(vind)*s(3) + (1-s(3))*( Fe.pre(vind)*s(2) + Fe.nxt(vind)*(1-s(2)) ) ;
	%res = round(res);
	mean(res)
	histogram(res(res<5),'BinWidth',0.01);
	%save('res.mat','res');